
CREATE SEQUENCE SEQ_INSTRUCTOR      START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_SECTION         START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_FILTER          START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_GENERATED_SCHED START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_FAV             START WITH 1 INCREMENT BY 1;


CREATE TABLE STUDENT (
    ST_ID     VARCHAR2(20)  PRIMARY KEY,
    S_EMAIL   VARCHAR2(100) NOT NULL UNIQUE,
    PHONE_NUM VARCHAR2(20)
);


CREATE TABLE COURSE (
    C_ID      VARCHAR2(20)  PRIMARY KEY,
    C_NAME    VARCHAR2(200) NOT NULL,
    C_HRS     NUMBER(2)     NOT NULL,
    AVG_GPA   NUMBER(4,2),
    IS_ONLINE CHAR(1)       DEFAULT 'N' CHECK (IS_ONLINE IN ('Y','N','B')),
    C_TYPE    VARCHAR2(2)   NOT NULL    CHECK (C_TYPE IN ('CP','CS','CU','EU','EP'))
);

CREATE TABLE INSTRUCTOR (
    I_ID   NUMBER        PRIMARY KEY,
    I_NAME VARCHAR2(100) NOT NULL
);

CREATE TABLE SECTION (
    SEC_ID NUMBER      PRIMARY KEY,
    S_TIME VARCHAR2(5) NOT NULL,
    F_TIME VARCHAR2(5) NOT NULL,
    C_ID   VARCHAR2(20) NOT NULL REFERENCES COURSE(C_ID),
    I_ID   NUMBER       NOT NULL REFERENCES INSTRUCTOR(I_ID)
);

CREATE TABLE DAY_GROUP_SECTION (
    SEC_ID NUMBER       NOT NULL REFERENCES SECTION(SEC_ID),
    DAY    VARCHAR2(3)  NOT NULL CHECK (DAY IN ('Sun','Mon','Tue','Wed','Thu','Fri','Sat')),
    C_ID   VARCHAR2(20) NOT NULL REFERENCES COURSE(C_ID),
    PRIMARY KEY (SEC_ID, DAY)
);

CREATE TABLE STUDENT_COURSE (
    ST_ID VARCHAR2(20) NOT NULL REFERENCES STUDENT(ST_ID),
    C_ID  VARCHAR2(20) NOT NULL REFERENCES COURSE(C_ID),
    PRIMARY KEY (ST_ID, C_ID)
);

CREATE TABLE FILTER (
    F_ID      NUMBER        DEFAULT SEQ_FILTER.NEXTVAL PRIMARY KEY,
    ST_ID     VARCHAR2(20)  NOT NULL REFERENCES STUDENT(ST_ID),
    S_TIME    VARCHAR2(5)   NOT NULL,
    F_TIME    VARCHAR2(5)   NOT NULL,
    MIN_BREAK NUMBER(4)     DEFAULT 0,
    MAX_BREAK NUMBER(4)     DEFAULT 120
);

CREATE TABLE DAY_GROUP_FILTER (
    F_ID NUMBER      NOT NULL REFERENCES FILTER(F_ID),
    DAY  VARCHAR2(3) NOT NULL CHECK (DAY IN ('Sun','Mon','Tue','Wed','Thu','Fri','Sat')),
    PRIMARY KEY (F_ID, DAY)
);

CREATE TABLE INSTRUCTOR_ADDED (
    I_ID  NUMBER       NOT NULL REFERENCES INSTRUCTOR(I_ID),
    C_ID  VARCHAR2(20) NOT NULL REFERENCES COURSE(C_ID),
    ST_ID VARCHAR2(20) NOT NULL REFERENCES STUDENT(ST_ID),
    PRIMARY KEY (I_ID, C_ID, ST_ID)
);

CREATE TABLE GENERATED_SCHEDULE (
    SCHED_ID      NUMBER DEFAULT SEQ_GENERATED_SCHED.NEXTVAL PRIMARY KEY,
    F_ID          NUMBER NOT NULL REFERENCES FILTER(F_ID),
    CREATION_DATE DATE   DEFAULT SYSDATE NOT NULL
);

CREATE TABLE GENERATED_SECTION (
    SEC_ID   NUMBER       NOT NULL REFERENCES SECTION(SEC_ID),
    SCHED_ID NUMBER       NOT NULL REFERENCES GENERATED_SCHEDULE(SCHED_ID),
    C_ID     VARCHAR2(20) NOT NULL REFERENCES COURSE(C_ID),
    PRIMARY KEY (SEC_ID, SCHED_ID)
);

CREATE TABLE FAVOURITE (
    FAV_ID   NUMBER DEFAULT SEQ_FAV.NEXTVAL PRIMARY KEY,
    F_ID     NUMBER NOT NULL REFERENCES FILTER(F_ID),
    SCHED_ID NUMBER NOT NULL REFERENCES GENERATED_SCHEDULE(SCHED_ID),
    UNIQUE (F_ID, SCHED_ID)
);

CREATE INDEX IDX_SECTION_COURSE ON SECTION(C_ID);
CREATE INDEX IDX_SECTION_INSTR  ON SECTION(I_ID);
CREATE INDEX IDX_SC_STUDENT     ON STUDENT_COURSE(ST_ID);
CREATE INDEX IDX_FILTER_STUDENT ON FILTER(ST_ID);
CREATE INDEX IDX_GS_FILTER      ON GENERATED_SCHEDULE(F_ID);
CREATE INDEX IDX_GSEC_SCHED     ON GENERATED_SECTION(SCHED_ID);
CREATE INDEX IDX_FAV_FILTER     ON FAVOURITE(F_ID);
CREATE INDEX IDX_IA_STUDENT     ON INSTRUCTOR_ADDED(ST_ID);


